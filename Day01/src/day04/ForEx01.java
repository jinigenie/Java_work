package day04;

public class ForEx01 {
	public static void main(String[] args) {
		
		int sum = 0;
		
		for(int i = 1; i <= 10 ; i++) {
			
			sum += i;
			
		}
		System.out.println(sum);
		
		System.out.println("-------------------------");
		
		for(int j = 10; j >= 1; j--) {
			System.out.println(j);
		}
		
	}
}
